// Arithmetic Operators in JavaScript

// Addition
let sum = 10 + 5; // 15
console.log("Addition:", sum);

// Subtraction
let difference = 10 - 5; // 5
console.log("Subtraction:", difference);

// Multiplication
let product = 10 * 5; // 50
console.log("Multiplication:", product);

// Division
let quotient = 10 / 5; // 2
console.log("Division:", quotient);

// Modulus (Remainder)
let remainder = 10 % 3; // 1
console.log("Modulus:", remainder);

// Exponentiation
let power = 2 ** 3; // 8
console.log("Exponentiation:", power);

// Increment
let increment = 5;
increment++; // 6
console.log("Increment:", increment);

// Decrement
let decrement = 5;
decrement--; // 4
console.log("Decrement:", decrement);

//logical operators in JavaScript
// AND (&&)
let a = true;
let b = false;
let andResult = a && b; // false
console.log("AND:", andResult);
//or (||)
let orResult = a || b; // true
console.log("OR:", orResult);
//not (!)
let notResult = !a; // false
console.log("NOT:", notResult);
//bitwise operators in JavaScript
// Bitwise AND (&)
let bitwiseAnd = 5 & 3; // 1 (0101 & 0011 = 0001)
console.log("Bitwise AND:", bitwiseAnd);